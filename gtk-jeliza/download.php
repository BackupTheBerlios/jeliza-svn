„Ich“ ist der Titel des 34. Bandes von [[Karl May]]s gesammelten Werken, erschienen im Karl May Verlag. Der Band enthält zum Teil von fremder Hand bearbeitete autobiographische Schriften, u. a. die [[1910]] erschienene Autobiographie ''Mein Leben und Streben''.

„Ich Ich Ich“ ist ein Roman von [[Robert Gernhardt]] (1937-2006), der 2003 bei S. Fischer erschien.

„Ich“ ist ein Roman von [[Hilbig|Wolfgang Hilbig]], der 1993 erschien und autobiografische Züge hat.

Am südlichen Mainufer in [[Frankfurt am Main|Frankfurt]] steht das „Ich“-Denkmal, das von [[Hans Traxler]] entworfen und am 24. März 2005 eingeweiht wurde. Auf einer Tafel hat Traxler seine Idee illustriert, dass den Denkmalsockel jeder  benutzen kann, um sich darauf fotografieren zu lassen, und als Kommentar hinzugefügt: ''Jeder Mensch ist einzigartig. Das gilt natürlich auch für alle Tiere.''.

„[[Das Ich]]“ ist eine deutschsprachige Musikgruppe.

==„Ich“ als Wort==
Als Wort spielt „Ich“ in der Kommunikation eine spezielle Rolle, wo es in Beziehung zu einem „[[Du]]“ tritt oder „etwas“ über eine Sache oder 3. Person mitteilt. Für Psychologie oder Soziologie und in Gesprächen ist es interessant, ob und wann das „Ich“ umschrieben wird (etwa durch „man“ oder „[[wir]]“), und wieweit dies mit Unsicherheit und Selbstwertgefühl zu tun hat. Einige Sprecher verwenden so anstelle von &quot;ich&quot; das [[Indefinitpronomen]] &quot;man&quot;, um die eigene Situation zu verallgemeinern, zum Beispiel bei regelmäßigen Abläufen. &quot;Man steht spät auf, isst Mittag und ist immer noch müde.&quot; Siehe auch [[Grammatik]], [[Singular]].

==Zitat==
Das [[Leib-Seele-Problem]] ist eine philosophische Fragestellung, die sich mit der Konzeption des ''Ichs'' auseinandersetzt: Bin ''ich'' nur mein [[Körper]] als ''Biomaschine'', oder besitze ich eine [[Seele]], die eventuell sogar unabhängig von der materiellen Substanz von ''mir'' existiert. Auch die [[Neurologie]] und die [[Hirnforschung]] vermögen diese Frage noch nicht abschließend zu beantworten, und so bleibt die Fragestellung weiterhin eine Domäne der [[Metaphysik]] und der [[Religion]]. Siehe auch bei [[Immanuel Kant]]:

:''Ich, als denkend, bin ein Gegenstand des innern Sinnes und heiße Seele. Dasjenige, was ein Gegenstand äußerer Sinne ist, heißt Körper.'' - Immanuel Kant ([[Kritik der reinen Vernunft]], B 400)

