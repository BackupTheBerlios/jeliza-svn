=== Einzeln- oder zusammenstehende Bäume ===
[[Bild:Kiefer-1.jpg|thumb|Waldkiefer bzw. Föhre aus der Froschperspektive]]

Von großem Einfluss auf die Wuchsform ist bei allen Bäumen der Umstand, ob sie frei stehen oder eng mit anderen Bäumen zusammen aufgewachsen sind. So kann zum Beispiel bei der [[Rotbuche]], wenn sie frei steht, eine Krone schon in geringer Entfernung vom Boden entstehen, indem hier nicht selten wenig über Mannshöhe die ersten, horizontal ausstreichenden Äste sich am Stamm ausbilden.
Im geschlossenen Rotbuchenwald dagegen tragen die säulenförmigen Stämme erst in sehr beträchtlicher Höhe spitzbogenartig aufstrebende Äste, auf denen sich erst dann das Laubdach über den hohen Säulenhallen ausbreitet.
Ähnliche Verhältnisse zeigen auch meistens die anderen Bäume bei freiem und bei geschlossenem Stand. Daneben finden sich bei einigen Varietäten auch so genannte „Trauerbäume“, bei denen sämtliche Zweige zur Erde niederwachsen. Die bekannteste „trauernde“ Varietät ist die Traueresche; doch kennt man auch bei vielen anderen Bäumen „Trauerbäume“, so beispielsweise die [[Hängebuche|Trauerbuche]] oder ähnliche Formen bei den Birken, Ulmen, Linden, Weiden, etc.

== Bäume in unterschiedlichen Klimaten ==

In den [[Tropen]] findet sich der üppigste Baumwuchs; zudem sind es lauter eigentümliche Baumarten, welche dort die [[Urwald|Urwälder]] bilden. Sie gehören vorzugsweise den Familien der [[Palmen]], [[Wolfsmilchgewächse]] (Euphorbiaceae), [[Brennnesselgewächse]] (Urticaceae), [[Seifenbaumgewächse]] (Sapindaceae), [[Bombacaceae]], [[Byttneriaceae]], [[Mahagonigewächse]] (Meliaceae), [[Hülsenfrüchtler]] (Fabaceae) und [[Sapotaceae]] an.

In der [[Subtropen|subtropisch]]en Zone findet man Bäume unter den immergrünen [[Myrtengewächse]]n (Myrtaceae) und [[Lorbeergewächse]]n (Lauraceae) sowie [[Silberbaumgewächse]]n (Proteaceae), denen sich in der wärmeren gemäßigten Zone andere immergrüne Bäume anschließen, so die immergrünen [[Eiche]]n, [[Granatbaum|Granatbäume]], [[Orange (Frucht)|Orangen]] und [[Zitrone]]n, [[Ölbaum|Ölbäume]], [[Feigen]] sowie [[Myrte]] und [[Lorbeer]]. Dagegen sind in der kälteren gemäßigten Zone die laubwechselnden Bäume vorherrschend. [[Eichenwald|Eichen]]- und [[Buchenwald (Landschaft)|Buchenwälder]], [[Linden (Botanik)|Linden]], [[Ulmen]], [[Eschen]], [[Pappeln]], [[Weiden (Botanik)|Weiden]] sind hier charakteristisch.

Und obgleich auch hier bereits [[Nadelhölzer]] in zusammenhängenden Waldungen auftreten, werden diese doch erst in der [[Subarktis|subarktischen]] Zone eigentlich vorherrschend, wo die [[Laubbäume]] einer nach dem anderen verschwinden.
Überhaupt werden die Bäume, je mehr man sich den [[Polarkreis]]en nähert, geringer an Zahl und kleiner. [[Eiche]]n, [[Linden (Botanik)|Linden]], [[Esche]]n, [[Ahorne]] und [[Buche]]n hören in [[Schweden]] schon diesseits des 64. Grades nördlicher Breite auf.
Jenseits dieser Breite besteht die Baumvegetation hauptsächlich aus [[Fichten]] und [[Tannen]], die in zusammenhängenden Waldungen nordöstlich noch über den 60. Grad hinausreichen,  sowie aus [[Birken (Pflanze)|Birken]], die in zusammenhängenden Waldungen sich fast bis zum 71. Grad nördlicher Breite erstrecken, und zum Teil aus Ellern und [[Weiden (Botanik)|Weiden]].

Auch die Höhe über dem [[Meeresspiegel]] hat auf die Ausbreitung und Höhe der Bäume, natürlich im Verhältnis zur Entfernung vom [[Äquator]] und zum [[Klima]], bedeutenden Einfluss. In den [[Anden]] finden sich noch bis 94&amp;nbsp;m unter der Schneelinie ansehnliche Bäume; bis 2825&amp;nbsp;m Höhe gedeihen noch [[Wachspalmen]], mehrere [[Cinchonen]] und [[Eskallonien]]. Unter 30 Grad nördlicher Breite, wo die Schneegrenze bei 4048–4080&amp;nbsp;m liegt, kommen auf dem [[Himalaja]], nördlich von [[Indien]], noch in 3766&amp;nbsp;m Höhe Baumgruppen vor, die aus Eichen und Fichten bestehen.
Ebenso sind in [[Mexiko]], unter 25–28 Grad nördlicher Breite, die Gebirge bis 3766&amp;nbsp;m mit Fichten und bis 2825&amp;nbsp;m hoch mit mexikanischen Eichen bedeckt. In den [[Alpen]] des mittleren Europas hört der Holzwuchs bei einer Höhe von 1570&amp;nbsp;m, im [[Riesengebirge]] bei 1193&amp;nbsp;m und auf dem [[Brocken]] bei 1005&amp;nbsp;m auf.
Eichen und Tannen stehen auf den [[Pyrenäen]] noch bis zu einer Höhe von 1883&amp;nbsp;m; dagegen wächst die Fichte auf dem [[Sulitelma]] in [[Lappland]], bei 68 Grad nördlicher Breite, kaum in einer Höhe von 188&amp;nbsp;m, die [[Birken|Birke]] kaum in einer von 376&amp;nbsp;m.

== Schäden an Bäumen ==
[[Bild:Windbruch.jpg|thumb|Windbruch von Fichten]]
Hierunter fallen vor allem Insektenschäden, Windbruch (Baumteile brechen ab), Windwurf (der Baum wird mit den Wurzeln aus dem Boden gehebelt), Schneebruch (Baumteile unter schweren Schneelasten brechen ab), Blitzschaden (Stammteile werden abgesprengt), [[Frost]] (Trockenschaden durch Transpiration bei gefrorenem Boden, Stammrisse) und bei Jungbäumen übermäßiger [[Wildverbiss]] und [[Schälung]]. Die verschiedenen Krankheiten, von denen Bäume befallen werden können, bezeichnet man als Brand, Krebs, Grind oder Schorf, Baumkrätze, Rost, [[Mehltau]], Rot- oder Kernfäule, Gelbsucht, Harzfluss, Gummifluss, Darrsucht, Wassersucht sowie das Aufspringen der Rinde.

Zu Missbildungen an Bäumen zählt man die [[Maserkropf|Maserkröpfe]], die [[Hexenbesen (Biologie)|Hexenbesen]] oder Wetterbüsche sowie die [[Pflanzengalle|Gallen]].

== Nutzung ==

[[Bild:Expo 2000 Dach Holzkonstruktion.jpg|thumb|Baumstämme von [[Weißtanne]]n aus [[Gersbach (Südschwarzwald)]] stützen auf der Expo 2000 das größte freitragende Holzdach der Welt]]
Die Menschheit versorgen Bäume durch den Vorgang der [[Photosynthese]] mit dem für sie so wichtigen Atemgas Sauerstoff und durch Holzbildung mit nachwachsendem Rohstoff für Bauwerke sowie zur Energiegewinnung (Energieumwandlung).

Baumkulturen zum Zweck der Gewinnung von Holz, Zweigen, Rinden, Laub, Blüten, Früchten, Samen oder einzelnen chemischen Bestandteilen ([[Terpentin]], [[Zucker]], [[Kautschuk]], [[Balsam]]e, [[Alkaloide]] etc.) bilden einen Teilbereich der [[Forstwirtschaft]], der Landschafts- und Nutzgärtnerei. Dieser Anbau erfolgt auch heute häufig noch in Form von [[Plantage]]n. Zu diesem Zweck werden seit Generationen zum Wohle der Menschheit in der Pflanzenzucht Klone von Bäumen erzeugt. Dies erfolgt durch die Verbreitung und Vermehrung etlicher Obstsorten mittels [[Pfropfen]], wobei die bekannten, gewollten Eigenschaften der Früchte einer Obstsorte auf einen jungen Baum übertragen werden. Zurückgegangen ist dagegen die Nutzung von [[Streuobstwiese]]n, die früher in vielen Gebieten Mitteleuropas landschaftsprägend waren.

Mit der Lehre von den Bäumen (Gehölzen), welche in einem bestimmten Land im Freien gedeihen, beschäftigt sich die [[Dendrologie]]. Anpflanzungen von Bäumen in systematischer oder pflanzengeographischer Anordnung, die [[Arboretum|Arboreten]], dienen ihr zu Beobachtungs- und Versuchszwecken. Bäume können [[Vegetative Vermehrung|vegetativ]], das heißt durch Pflanzenteile, oder generativ durch [[Saat|Aussaat]] vermehrt werden. Bei der [[Pflanzung]] von Gehölzen in Garten und Landschaft sollten Pflanzregeln eingehalten werden.

== Stoffwechsel ==
[[Bild:Blatt_einer_Buche.jpg|thumb|Eine 80jährige Buche hat etwa 800.000 Blätter]]
Am Beispiel einer 80-jährigen, allein stehenden [[Rotbuche]]:

In diesem Lebensalter ist der Baum 25&amp;nbsp;m hoch, und seine [[Baumkrone]] mit einem Durchmesser von 15&amp;nbsp;m bedeckt eine Standfläche von 160&amp;nbsp;m². In ihren 2.700&amp;nbsp;m³ Rauminhalt finden sich 800.000 Blätter mit einer gesamten Blattoberfläche von 1.600&amp;nbsp;m², deren Zellwände zusammen 160.000&amp;nbsp;m² Fläche betragen.

Pro Stunde verbraucht diese Buche 2,352&amp;nbsp;kg [[Kohlenstoffdioxid]], 0,96&amp;nbsp;kg [[Wasser]] und 25.435&amp;nbsp;[[Joule]] ([[Solarenergie]]), im gleichen Zeitraum stellt sie 1,6&amp;nbsp;kg [[Traubenzucker]] her und deckt mit 1,712&amp;nbsp;kg [[Sauerstoff]] den Verbrauch von zehn Menschen. 

Die 15&amp;nbsp;m³ [[Holz]] des Baumes wiegen trocken 12.000&amp;nbsp;kg, allein&amp;nbsp;6.000 kg davon sind [[Kohlenstoff]].

Solche Beispiele dürfen jedoch nicht ohne Weiteres auf Bäume im Wald angewendet werden, denn der effektive Wirkungsquerschnitt eines freistehenden Baumes für die Photosynthese ist deutlich größer als die Standfläche, die wiederum größer ist als der effektive Wirkungsquerschnitt des Waldbaumes –&amp;nbsp;da die Sonne in Mitteleuropa nicht senkrecht scheint. Zudem verbrauchen die Bäume einen kleinen Teil des Sauerstoffes für Wachstums- und Erhaltungsprozesse.

== In Mitteleuropa heimische und häufige Baumarten ==
[[Image:Skarbin Laerchen Mischwald 17112006 03.jpg|thumb|Lärchen-Mischwald im Herbst]]
Zu den in [[Mitteleuropa]] heimischen [[Laubbaum|Laubbäumen]] zählen die [[Ahorne]], [[Birken (Pflanze)|Birken]], [[Buchen]], [[Eichen]], [[Erlen (Botanik)|Erlen]], [[Eschen]], [[Linden (Botanik)|Linden]], [[Mehlbeeren]], [[Pappeln]], [[Ulmen]] und [[Weiden (Botanik)|Weiden]].

Typische [[Nadelbaum|Nadelbäume]] sind die [[Fichten]], [[Kiefern]], [[Lärchen]], [[Tannen]] und [[Eiben]].

In Mitteleuropa häufig vorkommende Baumarten, der in diesem Gebiet ursprünglich nicht beheimatet sind, sind die [[Gewöhnliche Robinie]], der [[Walnussgewächse|Walnuss]]baum und viele [[Obstbaum|Obstbäume]]. Sie alle sind [[Neophyt]]en.

Eine detaillierte Aufstellung bietet die [[Liste von Bäumen und Sträuchern in Mitteleuropa]].

== Extreme Bäume ==
[[Bild:BonsaiTridentMaple.jpg|thumb|[[Bonsai]]]]
* Der ''höchste'' Baum der Welt ist der „Hyperion“, ein [[Küstenmammutbaum]] (''Sequoia sempervirens'') im [[Redwood-Nationalpark]] in [[Kalifornien]] mit 115,5&amp;nbsp;m Wuchshöhe.
* Der ''höchste'' Baum Deutschlands ist eine 62,45&amp;nbsp;m (Stand: 18. Juli 2006) hohe [[Douglasie]] (''Pseudotsuga menziesii'') im [[Eberbach]]er Stadtwald&lt;ref&gt;[http://www.n-tv.de/690573.html Eberbach hat den Längsten]&lt;/ref&gt;. Er löste damit die 100 Jahre alte „Douglasie Nr. 3“ ab, die in einem [[Forst]] nördlich des Tennenbacher Tals im Kreis [[Emmendingen]] bei [[Freiburg im Breisgau]] steht.
* Die ''niedrigsten'' Bäume sind [[Bonsai]], die durch menschliche Eingriffe künstlich klein gehalten werden.
* Die ''ältesten Bäume'' sind - gemäß verbürgter Jahresringzählung -  über 4800 Jahre alte [[Langlebige Kiefer]]n (''Pinus longaeva'', früher als Varietät der [[Grannenkiefer]] angesehen) in den White Mountains in Kalifornien &lt;ref&gt;Strasburger: ''Lehrbuch der Botanik.'' S. 423&lt;/ref&gt;. 
* Der ''dickste'' Baum ist der „[[Baum von Tule]]“, eine [[Mexikanische Sumpfzypresse]] (''Taxodium mucronatum'') in [[Santa Maria del Tule]] im [[Mexiko|mexikanischen]] Staat [[Oaxaca]]. Sein Durchmesser an der dicksten Stelle beträgt 11,42&amp;nbsp;m.
* Die ''winterhärtesten'' Bäume sind die [[Dahurische Lärche]] (''Larix gmelinii'') und die [[Ostasiatische Zwerg-Kiefer]] (''Pinus pumila''): Sie widerstehen Temperaturen bis zu &amp;minus;70&amp;nbsp;°C.
* Die Dahurische Lärche ist auch die Baumart, die am ''weitesten im Norden'' überleben kann: 72° 30' N, 102° 27' O.
* Der Baum, der die dünnste Luft atmet, ist die [[Schuppenrindige Tanne]] ''Abies squamata:'' sie wächst auf ''4600&amp;nbsp;m [[Seehöhe]]'' am Ost[[himalaya]] in [[Sichuan]].
* Das Holz ''geringster [[Dichte]]'' ist jenes des [[Balsabaum]]es.
* Bäume, die bis dahin ''kahle Flächen'' besiedeln können, so genannte Pionierbäume, sind zum Beispiel bestimmte [[Birken]]-, [[Weiden (Botanik)|Weiden]]- und [[Pappeln|Pappel]]arten.
