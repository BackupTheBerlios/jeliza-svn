== Bäume und Menschen ==
{{Zitat|Kein anderes Geschöpf ist mit dem Geschick der Menschheit so vielfältig, so eng verknüpft wie der Baum.}}
schrieb der Historiker [[Alexander Demandt]] und hat dem Baum mit ''Über allen Wipfeln – Der Baum in der Kulturgeschichte'' ein umfangreiches Werk gewidmet. Für ihn beginnt die Kulturgeschichte mit dem Feuer, das der Blitz in die Bäume schlug und mit dem Werkzeug, für das Holz zu allen Zeiten unentbehrlich war.
[[Bild:Caspar David Friedrich 005.jpg|thumb|[[Caspar David Friedrich]]; ''Bäume im Mondschein,'' Öl auf Leinwand, um 1824, [[Köln]], [[Wallraf-Richartz-Museum]]]]

=== Brauchtum ===
Dieser Bedeutung entsprechend, ist auch ein vielfältiges Brauchtum mit dem Baum verknüpft. Das reicht vom Baum, der zur Geburt eines Kindes zu pflanzen ist, über den [[Maibaum]], der in manchen Regionen immer noch in der Nacht zum ersten Mai der Liebsten verehrt, dem [[Kirmesbaum]] und dem [[Weihnachtsbaum]], unter dem gefeiert und dem [[Richtbaum]], der zur Feier eines neu errichteten Hauses auf dem Dachstuhl aufgesetzt wird, bis zum Baum, der auf dem Grab gepflanzt wird. Nationen und Völkern werden bestimmte, für sie charakteristische Bäume zugeordnet. [[Eiche]] und [[Linden (Botanik)|Linde]] gelten als typisch „deutsche“ Bäume. Die [[Birke]] symbolisiert Russland, und der [[Baobab]] gilt als der typische Baum der [[Afrika|afrikanischen]] Savanne.
In Bayern gibt es auch den Hungerbaum [[Hungerbaum]] welcher eine alte Tradition in Bayern darstellt.

Unter der [[Gerichtslinde]] wurde Recht gesprochen und unter der [[Tanzlinde]] gefeiert. [[Kelten]], [[Slawen]], [[Germanen]] und [[Balten]] haben einst in Götterhainen Bäume verehrt, und das Fällen solcher Götzenbäume ist der Stoff zahlreicher Legenden, die von der Missionisierung Nord- und Mitteleuropas berichten.

===  Mythologie und Religion ===
In vielen [[Mythos|Mythen]] existiert ein [[Weltenbaum]], der die Weltachse darstellt, um die der Kosmos gruppiert ist.

In vielen alten Kulturen und [[Religion]]en wurden Bäume oder Haine als Sitz der Götter oder anderer übernatürlicher Wesen verehrt. So spielt der Baum in den Mythen der Völker auch als Lebensbaum (zum Beispiel die [[Sykomore]] bei den Ägyptern oder der [[Baum des Lebens]] in der jüdischen Mythologie), als Baum der Unsterblichkeit (der Pfirsichbaum in China) oder als Symbol des [[Bodhi|Erwachens]] im [[Buddhismus]] (der [[Bodhibaum]]) eine Rolle.

==== In der Bibel ====
Auch in der [[Bibel]] werden Bäume immer wieder erwähnt. Das [[Altes Testament|Alte]] wie das [[Neues Testament|Neue Testament]] nennen unterschiedliche Baumarten wie zum Beispiel den [[Olivenbaum]] oder den [[Echte Feige|Feigenbaum]], mit dessen relativ großen Blättern das erste Menschenpaar [[Adam und Eva]] laut 1. Mose/Genesis 3:7 nach ihrem [[Sündenfall]] ihre [[Nacktheit|Blöße]] bedeckten. Im [[1. Buch Mose]], der ''Genesis,'' wird in Kapitel 1 in den Versen 11 und 12 berichtet, dass [[Gott]] die Bäume und insbesondere die fruchttragenden Bäume in seiner [[Schöpfung]] der Welt hervorbrachte. Zwei Bäume jedoch spielen in der Bibel eine entscheidende Rolle: der [[Baum des Lebens]] sowie der [[Baum der Erkenntnis|Baum der Erkenntnis von Gut und Böse]]. So hat der Baum auch in der christlichen [[Ikonographie]] eine besondere Bedeutung. Dem Baum als Symbol des Sündenfalls, um dessen Stamm sich eine Schlange windet, steht häufig das hölzerne Kreuz als Symbol der Erlösung gegenüber. Ein dürrer und ein grünender Baum symbolisieren in den [[Allegorese|Dogmenallegorien]] der [[Reformation]]szeit den [[Alter Bund|Alten]] und den [[Neuer Bund|Neuen Bund]]. In der [[Pflanzensymbolik]] haben verschiedene Baumarten wie auch ihre Blätter, Zweige und Früchte eine besondere Bedeutung. So weist die [[Akazie]] auf die Unsterblichkeit der menschlichen Seele hin, der [[Ölbaum]] auf den Frieden und ist ein altes [[Mariensymbole|marianisches Symbol]] für die Verkündigung an Maria. Der Zapfen der [[Pinie]] weist auf die Leben spendende Gnade und Kraft Gottes hin, die [[Stechpalme]], aus deren Zweigen nach der Legende die [[Dornenkrone]] gefertigt war, auf die Passion [[Jesus von Nazareth|Christi]].

