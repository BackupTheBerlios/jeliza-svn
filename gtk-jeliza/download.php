<textarea name='wpTextbox1' id='wpTextbox1' cols='80' rows='25' readonly='readonly'>{{Dieser Artikel|behandelt die pflanzliche Lebensform Baum. Für weitere Bedeutungen, siehe [[Baum (Begriffsklärung)]].}}
[[Bild:Sequoiadendron giganteum at Kenilworth Castle.jpg|right|thumb|300px|Der [[Riesenmammutbaum]] ''(Sequoiadendron giganteum)'' wird in der Regel 60–80&amp;nbsp;m hoch. Das höchste bekannt gewordene Exemplar maß 135&amp;nbsp;m.]]
Als '''Baum''' wird in der [[Botanik]] eine ausdauernde ([[mehrjährig]]e) [[Pflanzen|Pflanze]] bezeichnet, die einen deutlich erkennbar aufrechten, [[Verholzung|verholzten]] [[Pflanzenwuchsformen|Stamm]] besitzt, der aus einer [[Wurzel (Pflanze)|Wurzel]] emporsteigt und an dem sich oberirdisch [[Ast|Äste]] befinden. Diese wiederum bilden [[Zweig]]e, [[Blatt (Pflanze)|Blätter]], [[Blüte]]n und [[Frucht (Botanik)|Früchte]] aus.
Die Zweige verlängern sich jedes Jahr durch Austreiben von End[[knospe]]n, verholzen dabei und nehmen kontinuierlich an Dicke und Umfang zu. Im Gegensatz zum [[Strauch]] ist es das besondere Merkmal des Baumes, dass sein holziger [[Stamm]] erst in einer gewissen Höhe eine aus blättertragenden Ästen bestehende [[Baumkrone|Krone]] entwickelt.

== Pflanzengruppen, bei denen Stämme vorkommen ==
[[Bild:SenegalBaobab.jpg|left|thumb|[[Baobab]] – Charakterbaum der semi-ariden Savannen Afrikas]]
Baumförmige Lebensformen kommen in sechs verschiedenen Pflanzengruppen vor:

Echte Bäume sind die [[Laubbaum|Laubbäume]] (aus den [[Bedecktsamer]]n), die [[Nadelbäume]] (aus den [[Nadelholzgewächse]]n) sowie der Sonderfall ''[[Ginkgo]] biloba'' (einziger noch existierender Vertreter der [[Ginkgogewächse]]). Diese drei Pflanzengruppen haben [[Holz|verholzte]] Stämme.

Daneben kommen drei kleinere Pflanzengruppen vor, die ''baumartige'' Strukturen ausbilden: die [[Palmengewächse|Palme]]n, die [[Palmfarne]] und die [[Baumfarne]].
Diese drei Gruppen besitzen kein echtes Holz mit [[Dickenwachstum]].
Daher ist der Stammdurchmesser relativ gleichmäßig.
Der Stamm entsteht aus den Blattansätzen.

Die Baumform findet sich hauptsächlich in rund 50 höheren Pflanzenfamilien. Dagegen fehlt die Baumform bei [[Algen]], [[Moose]]n, [[Liliengewächse]]n, [[Iridaceae]], [[Hydrocharitaceae]], [[Orchideen]], [[Chenopodiaceae]], [[Primelgewächse]]n und meist auch bei den [[Lamiales]], [[Convolvulaceae]], [[Enziangewächse]]n, [[Glockenblumengewächse]]n, [[Cucurbitaceae]], [[Doldengewächse]]n, [[Saxifragaceae]], [[Papaveraceae]], [[Ranunculaceae]] oder [[Caryophyllaceae]].
