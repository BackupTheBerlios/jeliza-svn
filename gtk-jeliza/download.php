=== Baumstamm: Morphologie baumförmiger Lebensformen ===
[[Bild:Buchenstamm.jpg|thumb|Mächtiger Buchenstamm]]
[[Bild:Baumstamm.jpg|thumb|Detailaufnahme eines Baumstammes]]
Baumartige Lebensformen haben eine unterschiedliche [[Morphologie (Biologie)|Morphologie]] (inneren Aufbau), und damit hängt zum Teil auch das charakteristische Aussehen zusammen. Assoziiert wird in der Regel der Aufbau aus Baumkrone, Baumstamm und Baumwurzeln. Doch die Wuchsformen sind vielfältiger.

Bei den baumartigen [[Farne]]n und den meisten [[Palmen]] findet sich ein einfacher Stamm, der mit einer einzigen großen Gipfelknospe endigt. Daher bilden sie keine Äste, sondern enden lediglich mit dicht übereinander stehenden, riesenhaften, meist gefiederten Blättern. Es sind damit per Definition keine echten Bäume.

Bei den echten Bäumen wächst der [[Spross]] des Keimpflänzchens heran bis zum Anfang des künftigen Baumstammes. Bei den Wuchsverhältnissen der Stämme bemerkt man aber in der Regel schon von den ersten Lebensjahren an zahlreiche Unterschiede.
Entweder bildet sich der Spross an der Spitze durch seine dauernd erhalten bleibende Gipfelknospe regelmäßig weiter und wird zum geraden, bis zur höchsten Spitze durchgehenden [[Baumstamm]]. Beispiele dafür sind [[Fichten|Fichte]], [[Tannen|Tanne]] und [[Lärchen|Lärche]], bei denen seitlich die zahlreichen horizontal abgehenden Äste ansetzen, welche die pyramidenförmige [[Baumkrone|Krone]] bilden. Diese wird schlanker, wenn sich auch die Äste steil am Stamm aufwärts richten, wie bei der italienischen Pappel.

Oder der Stamm zeigt zwar auch längere Zeit dieses Verhalten, doch später folgen ihm einer oder mehrere seiner Äste in bald schrägerer, bald steilerer Richtung sowohl im Höhenwuchs als in der Erstarkung nach oder überholen ihn oder übernehmen nach gänzlicher Unterdrückung des Hauptstammes allein die Fortbildung, so dass also der Stamm nicht bis in den Gipfel reicht, sondern sich in seiner Krone in mehrere starke Hauptäste teilt, wie bei der Kiefer, der Pappel, der Eiche, dem Apfelbaum und vielen anderen Bäumen.
Oder es verliert der Hauptspross schon in den ersten Lebensjahren die Zellteilungsfähigkeit der Endknospe. Die Seitenknospen übernehmen das Hauptwachstum und es bilden sich Seitenäste. Da dies alljährlich geschieht, baut sich hier der Stamm aus so vielen einzelnen auseinander hervorgegangenen Ästen verschiedenen Grades auf, wie er Jahre alt ist, und erscheint dann im erwachsenen Zustand ebenso regelmäßig und gerade wie diejenigen Stämme, welche durch stetige Verlängerung einer Hauptachse gebildet sind. Diese Stammbildung ist charakteristisch für die [[Ulmen|Ulme]], [[Rotbuche|Buche]], [[Hainbuche]], [[Linden (Botanik)|Linde]].

Hinsichtlich des inneren Baues des Baumstammes weichen die zu den [[Einkeimblättrige]]n gehörenden baumförmigen Lebensformen, nämlich die [[Palmen]], von den echten Bäumen erheblich ab. Bei ersteren stehen die Gefäßbündel im Grundgewebe zerstreut, weshalb es auch keinen [[Kambium]]ring, keinen Holzzylinder und somit auch kein fortdauerndes Dickenwachstum des Stammes gibt.
Bei den zu den Dikotyledonen gehörenden Bäumen besitzt der Stamm schon in der frühesten Jugend als dünner Stängel einen unter der [[Rinde]] gelegenen Kreis von [[Leitbündel]]n, welcher den Rindenbereich vom innen liegenden [[Mark (Botanik)|Mark]] scheidet.
Dieser Leitbündelring stellt in seiner inneren, dem Mark anliegenden Hälfte das [[Holz]] und im äußeren, an die Rinde angrenzenden Teil den [[Bast]] dar; zwischen beiden zieht sich der Kambiumring hindurch. Dieser wird aus zarten, saftreichen, sich ständig teilenden Zellen gebildet und vergrößert durch seinen laufenden Zellvermehrungsprozess die beiderseits ihm anliegenden Gewebe. So wird alljährlich an der Außenseite des Holzringes eine neue Zone Holzgewebe angelegt, wodurch die [[Jahresring]]e des auf diese Weise erstarkenden Holzkörpers entstehen, die man als konzentrische Linien am Stammquerschnitt wahrnimmt.
Andererseits erhält aber auch der weiter außen liegende Bast an seiner Innenseite einen jährlichen, wenn auch weit geringeren Zuwachs. Auf diese Weise kommt die dauernde Verdickung des Stammes und aller seiner Äste sowie auch der Wurzeln zustande.

=== Aufbau des Baumstammes ===
{| align=&quot;right&quot; border=&quot;0&quot; style=&quot;margin:0 0 2em 2em&quot; width=&quot;300px&quot;
| align=&quot;center&quot; style=&quot;font-size:90%&quot; |
[[Bild:Stamm.svg|280px]]

Querschnitt durch einen fünfjährigen [[Kiefer]]nstamm
|}
Ein Querschnitt durch einen Baumstamm zeigt verschiedene Holzarten. Ganz innen befindet sich das [[Mark (Botanik)|Mark]] und das abgestorbene Kernholz, das keine Nährstoffe mehr transportiert. Weiter außen befindet sich das [[Splintholz]], das sich bei so genannten ''Kernholzbäumen'' deutlich vom Kernholz abhebt; es ist dunkler und härter. Bei der Eibe ist dies sehr gut sichtbar. Bei ''Reifholzbäumen'' unterscheiden sich die beiden Holzsorten nur in der Härte, sind aber beide splintfarben. ''Splintholzbäume'' wie zum Beispiel die Birke besitzen nur Splintholz. Ein weiterer Unterschied zwischen Splint- und Kernholz, der vor allem beim [[Bogen (Waffe)|Bogen]] ausgenutzt wird, ist der, dass sich Splintholz gut dehnen und Kernholz gut zusammendrücken lässt –&amp;nbsp;umgekehrt aber nicht.

Die äußerste Schicht bildet die [[Rinde|Baumrinde]]. Sie besteht aus der [[Bast (Baum)|Bastschicht]], die in Wasser gelöste Nährstoffe transportiert, und der [[Borke]], die den Baum vor Umwelteinflüssen und Schädlingen schützt.

Zwischen der Bastschicht und dem Holz befindet sich das [[Kambium]]. Diese Wachstumsschicht bildet das Holz und die Bastschicht.

=== Die Wurzel ===

Auch in der [[Wurzel (Pflanze)|Wurzel]]bildung unterscheiden sich die Bäume. Manche behalten die Hauptwurzel, die sich am Keimpflänzchen entwickelt, ihr ganzes Leben hindurch. Die Hauptwurzel wächst dann als gerade, dicke Pfahlwurzel tief in den Boden hinab, was besonders für die [[Eiche]] charakteristisch ist.
In anderen Fällen bleibt die Pfahlwurzel frühzeitig zurück; aus dem Stock entwickeln sich mehrere Seitenwurzeln, und diese wachsen entweder auch zu beträchtlicher Tiefe in schiefer Richtung in den Boden hinein, wie zum Beispiel bei der [[Linden (Botanik)|Linde]], oder sie halten sich nur oberflächlich und breiten sich dabei oft weit im Umkreis aus, wie bei den [[Pappeln|Pappelarten]].
Überdies erzeugt ein stets lockerer und tiefgrundiger Boden eine tiefere Wurzelausbildung. Ist die Bodenbeschaffenheit dagegen bindiger und flachgrundiger, kommt es zu einer oberflächlicheren Wurzelausbildung. Bei einigen Baumarten bilden diese flachen Wurzeln neue Triebe aus, so genannte [[Wurzelbrut]].
Die [[Einkeimblättrige|einkeimblättrigen]] baumförmigen Lebensformen haben nie eine Pfahlwurzel; ihr Stamm endet nahe unter der Bodenfläche und ist mit seitlich aus ihm hervorkommenden Nebenwurzeln im Erdreich befestigt.

=== Das Alter, der Stammumfang und die Höhe der Bäume ===

Die Bäume können bei ungestörter Vegetation und unter günstigen Verhältnissen ein außerordentliches Alter erreichen. Mit dem hohen Alter, das oft mehrere Jahrhunderte betragen kann, ist in der Regel eine ungewöhnliche Dicke des Stammes, aber nicht immer eine entsprechende Höhe verknüpft.

Wächst der Baum unter im [[Jahreszeiten|Jahresrhythmus]] schwankenden [[Klima|klimatischen Bedingungen]], wird jedes Jahr ein [[Jahresring]] angelegt. Mit Hilfe dieser Ringe lassen sich sowohl das Alter eines solchen Baumes als auch die Bedingungen in den einzelnen Jahren ablesen. Die [[Dendrochronologie]] nutzt dies, um altes Holz zu datieren und das Klima einer Region bis zu mehreren tausend Jahren zurückzuverfolgen.

