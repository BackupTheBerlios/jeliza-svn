'''Ich''' ist die Bezeichnung für die eigene separate [[Individuum|individuelle]] [[Identität]] einer menschlichen [[Natürliche Person|natürlichen Person]], zurückweisend auf das ''[[Selbst]]'' des Aussagenden.
:Beispiel: „Ich denke, also bin ich“, von [[René Descartes]]: ''[[cogito ergo sum]]''.
In der sprachlichen Verwendung eines Bezugs ist das Ich in den Begriff der [[Deixis]] (''Hier-Jetzt-Ich-Origo'') eingebunden, der die grammatikalische Funktion einer ersten Person in der [[Einzahl]] entspricht.

==Ich-Erleben==

Das '''Ich''' erlebt der Mensch innerhalb seines Wachbewusstseinszustandes als das eigene Sein in den Ausdrucksformen von Denken, Fühlen und Handeln und betrachtet sich insofern auch meist als den Urheber derselben. Das Ich wird daher als Persönlichkeitsmittelpunkt empfunden, der die Struktur der [[Person]] maßgeblich bestimmt. In diesem Sinne wird es aufgefasst als real seiender Quell oder Wesenskern von Eigen- oder Selbstschöpfung, dem darüberhinaus oft auch Anteil am Ewigem zugeschrieben wird.
Das Ich-Erleben beinhaltet die Empfindung des Menschen als eigenständige Ganzheit, als Unteilbares = [[Individualität]]. Damit betrachtet sich der Mensch als selbstkompetent, als mündiges Wesen, dem damit das Recht auf Selbstbestimmung grundsätzlich zu steht.
Somit bildet dieses Ich-Erleben einerseits die Grundlage für die Anerkennung der [[Menschenwürde]] und die der Gestaltung der [[Menschenrechte]], andererseits besteht durch das Ich-Erleben die Gefahr einer Egozentrik aus der heraus die eigenen Grenzen und die Grenzen des Anderen nicht mehr wahrgenommen werden und es so zu Grenzüberschreitungen und übertriebenen Raumausweitungen kommt, welche meist Leiderfahrungen, [[Konflikte]] zwischen Einzelnen als auch von Interessensgruppen gegeneinander, nach sich ziehen.
Im [[Tiefschlaf]] ist dagegen kein Ich-Erleben möglich - und damit stellt sich auch die ungewisse Frage, in wieweit es nach dem [[Tod]] ein Ich-Erleben geben kann - bzw. die weitere: ob Ich gleich Ich-Erleben ist bzw. ob es verschiedene Formen von Ich-Erleben gibt. Im Ich-Erleben ist auch das Erleben des Ich-Zweifels zugegen: bin ich der, für den ich mich halte - oder habe ich nur ein Ich-Bild von mir (meinem Ich) - bzw. bin Ich nur Vorstellung, gibt es das Ich evtl. überhaupt nicht als reales Sein? 

Wie also einerseits das Ich als das Übergreifende, Zusammenhangschaffende, Integrierende erlebt wird, so scheint es andererseits auch diese Zweifel, diese Spaltung von sich selbst zu produzieren. Und aufgrund dieses erlebten Zwiespaltes kommt es zu den entsprechend unterschiedlichen Auffassungen über das Ich...
			

==Das Ich in Wissenschaft und Psychologie==

Die Kategorie des Ich findet besondere Berücksichtigung im philosophischen System [[Johann Gottlieb Fichte]]s, mit zumindest wissenschaftlichem Anspruch angegangen wurde es erstmals in der [[Psychoanalyse]] [[Sigmund Freud]]s.

===Das Ich in der Psychoanalyse Freuds===

[[Sigmund Freud]] war der erste, der sich mit dem Ich psychologisch ausführlich befasste. Seiner Meinung nach ist die menschliche [[Psyche]] in drei Teile geteilt ([[Drei-Instanzen-Modell]]):

*Das ''[[Es (Psychoanalyse)|Es]]'', der vegetative Teil der Psyche, der meist im Unbewussten verbleibt und die grundlegenden [[Instinkt]]e und [[Trieb]]e des Menschen umfasst.

*Das ''[[Über-Ich]]'', das die Funktion des [[Gewissen]]s einnimmt und das Ich leitet [[Ideal-Ich]]. Es wird von Freud als das Überbleibsel der elterlichen [[Autorität]] in der Kindheit angesehen.

*Das ''Ich'', mit dem Freud das bewusst Erfahrene bezeichnet. Dieses Ich wird sowohl vom Über-Ich als auch vom Es beeinflusst und vermittelt zwischen diesen beiden Instanzen.

===Das Selbst in anderen Bereichen der Psychologie===
Die Erforschung des [[Selbst]] ist ein wichtiger Bestandteil der modernen Psychologie. Spezielle Forschungsbereiche umfassen z. B. die Bedeutung des [[Selbstwert]]es, der [[Selbstsicherheit]], der [[Selbstkontrolle]] oder der [[Selbstwirksamkeit]]&lt;nowiki&gt;&lt;/nowiki&gt;serwartung.

===Das Ich im Symbolischen Interaktionismus===
Einen großen Stellenwert nahm das Ich in der in den USA entwickelten [[Mikrosoziologie|mikrosoziologischen]] Theorie des [[Symbolischer Interaktionismus|Symbolischen Interaktionismus]] ein. Diese Theorie ging von der philosophischen Richtung des [[Pragmatismus]] aus, die den Menschen als ein aktives Wesen bezeichnet, das sich seine Welt mittels Interaktion mit ihr selbst [[Konstruktivismus|konstruiere]]. Mit anderen Worten: Ohne das Individuum existiere die Welt nicht.

Im Symbolischen Interaktionismus sind die Theorien von [[Charles Cooley]], [[George Herbert Mead]] und [[Erving Goffman]] richtungsweisend.

[[Charles Cooley]] war der erste, der sich mit dem Ich im Rahmen dieser Theorie beschäftigte. Für ihn entsteht das Selbst bzw. das Ich einzig und allein in der Interaktion des Individuums mit seiner Umwelt. Sein Modell wird auch [[Spiegel-Ich]] genannt, da sich das Individuum seiner Theorie zufolge nach der Weise definiert, wie es von anderen Menschen wahrgenommen wird.

[[George Herbert Mead]] ging von einer ähnlichen Theorie aus, nach ihm gibt es jedoch zwei Dimensionen des Ich, das I und das ME. Das ME entspricht in etwa dem Spiegel-Ich Cooleys, es besteht aus der Reflexion mit dem Umweg über die Gesellschaft in Form von Normen und Regeln. Das I jedoch ist eine autonome, unvorhersehbare, individuelle Dimension des Ich. Hier befindet sich laut Mead die menschliche Kreativität. I und ME befinden sich in einer permanenten Interaktion untereinander.

[[Erving Goffman]] sieht das Ich dagegen in seinem sogenannten ''Dramaturgischen Modell'' als eine Art Schauspieler an, das in verschiedenen Situationen verschiedene Formen annimmt. Laut Goffmann ist es unmöglich, das Ich einer Person wirklich zu definieren, da dieses Ich auch in der Selbstreflexion verschiedene Rollen annehmen kann.

===Physikalische Theorien über das Ich===
Derzeit steht die Physik noch mit der Frage, ob und wie es ein Ich im Menschen auch physikalisch gibt, vor einem großen Rätsel. Es gibt zwar mehrere Theorien, von denen aber der Großteil als reine Spekulationen eingestuft werden müssen. Es gibt sogar Wissenschaftler, die behaupten, das menschliche Gehirn sei nicht fähig, sich selbst zu erkennen, also zu definieren, was das Ich ist.

===Neurobiologische Aspekte===
Früher dachte man, das Ich sei auf ein bestimmtes [[Hirnareal]] beschränkt. Als heute einigermaßen anerkannte Theorie kann man dagegen die Theorie des [[Bindungprinzip]]s ansehen. Diese Theorie geht davon aus, dass sich das Ich auf das gesamte Gehirn (eventuell auch auf das gesamte [[Nervensystem]]) ausdehnt, wobei die [[Nervenzelle]]n über einen noch nicht verstandenen Mechanismus miteinander interagieren und sich so als Ganzheit vereinigen (die dann mehr als die Summe ihrer Teile ist).

==Das Selbst im spirituellen und philosophischen Bereich==
Das Transzendieren, die bewusste Klärung von Ich ([[Ego]]) und Selbst, ist das Hauptthema und Ziel im [[Hinduismus]] und im [[Buddhismus]]. Der Schüler (Tschela) eines geistigen Weges im Hinduismus ([[Yoga]]) erkennt, dass sein Ich sich im „inneren Selbst“ (dem ''[[Atman]]'') auflöst und damit die Einheit mit dem Göttlichen ([[Brahman]]) als Selbsterkenntnis stattfindet. 
Im Buddhismus hingegen wird die Existenz einer Seele und von etwas Göttlichem abgestritten, alle Phänomene sind letztendlich Leerheit und der Weg ist lediglich ein Erwachen zur Erkenntnis der Realität.

Dieses Erlebnis wird [[Samadhi]] genannt, im [[Buddhismus in Japan|japanischen Buddhismus]] [[Satori]]. Alle Yogapraxis (Jnana-Yoga, Raja-Yoga) dient nur dazu, diese Täuschung einer eigenen separierten Existenz des Ichs (Egos) zu überwinden. Es gibt in der Erfahrung des eigenen Selbst das Licht-Erlebnis des ''Einen ohne ein Zweites'' ([[Erleuchtung]]&lt;nowiki&gt;&lt;/nowiki&gt;serlebnis).

Das Ich (Ego) gibt seine Täuschungs-Existenz auf und wird eins mit dem Ganzen (mit dem spirituellen Licht des ewigen Lebens). 
Tatsächlich „wird“ es nicht eins: Da das Ich (Ego) tatsächlich nie existiert hat, wird diese Einheit nach dem Loslassen von der Täuschung eines „Ichs“ als allumfassende Glückseligkeit im ewigen Licht erlebt. 

Im ursprünglichen ([[Theravada]]) Buddhismus existiert dieses spontane Erleuchtungserlebnis zwar auch, wird aber letztlich als Täuschung bzw. ohne bleibenden Wert begriffen. Das „kleine Tor“ (Lankavatara Sutra) des Erleuchtungserlebnisses ist dort lediglich ein erster Kontakt mit dem durch Übung zu beschreitenden Weg, und kein erstrebenswerter Zustand.

Entsprechende Licht-Erlebnisse haben Eingang in die religiöse Literatur aller Kulturen gefunden, obwohl sie nicht überall als zentrales Gotteserlebnis begriffen werden. Diese Selbsterfahrung wird auch in der Bibel bei Johannes beschrieben: &quot;Gott ist Licht und in ihm ist keine Finsternis.&quot; Weiter wird Jesus ein Satz in den Mund gelegt, womit er sich selbst, aber auch das Prinzip der Selbst-Erkenntnis meinte: &quot;Das Licht leuchtet in der Finsternis ([[Ego]]), und die Finsternis hat es nicht begriffen&quot;.
Auch der Satz über dem Eingang zum [[Orakel von Delphi]] ''Erkenne dich selbst!'', als [[Imperativ]] auch als Leitsatz dem [[Sokrates]] zugeschrieben, handelt von der [[Transzendenz]] des Egos hin zum göttlichen Selbst (Licht).

Im christlichen Bereich ist besonders  der [[Mystiker]] [[Joel S. Goldsmith]] (†1964) zu erwähnen, der die philosophischen Grundlagen für das Loslassen des menschlichen Ichs (Egos) hin zum göttlichen Selbst in seinen Büchern beschreibt:
* Das mystische Ich
* Der Donner der Stille
* The infinite way

In der Psychologie der [[Sufismus|Sufis]] ([[islam]]ische Mystiker) existieren sieben verschiedene Stufen des Selbst ([[Arabische Sprache|arabisch]]: ''[[nafs]]''), die unterste ist ''an-nafs al-ammara'', das ''niedere Selbst'', die höchste ''an-nafs al-safiya'', das ''reine Ich''. Dazwischen liegen die Stationen der Gottessuchenden auf dem Weg zur [[Gott|göttlichen]] Einheit ([[tauhid]]).

==Werke mit dem Titel „Ich“==
[[Bild:ich_denkmal.jpg|thumb|„Ich“-Denkmal]]

„Ich“ war der geplante Titel von [[Max Stirner]]s Hauptwerk, das 1844 als ''[[Der Einzige und sein Eigentum]]'' veröffentlicht wurde. Geblieben ist „Ich“ darin als Titel der ''Zweiten Abteilung'', die den größeren Teil des Buches ausmacht und Stirners Ruf als „Philosoph des Ich“ begründet hat.

