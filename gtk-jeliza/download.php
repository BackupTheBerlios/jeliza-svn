== Die besonderen Merkmale der Bäume ==

[[Bild:Illustration Ulmus carpinifolia0.jpg|thumb|right|Bei Blüten der Ulmen sind die Blütenhüllblätter reduziert, also bestehen sie im Wesentlichen aus dem Stempel und den Staubblättern, sind klein und unauffällig]]

=== Die Blätter ===

Die Gestalt der [[Blatt (Pflanze)|Blätter]] ([[Laub (Botanik)|Laub]]) ist ein wichtiges Bestimmungsmerkmal. So kann der Baum entweder [[Laubblatt|Laubblätter]] unterschiedlicher Formen oder [[Nadelblatt|Nadelblätter]] tragen.

Nicht minder brauchbar zur Unterscheidung im [[winter]]lichen Zustand sind die [[Knospe]]n des Baumes. Manche Bäume sind überdies mit [[Dorn|Dornen]] ausgestattet. Dies sind entweder kurze Zweige, die mit dorniger Spitze enden ([[Weißdorne]], Wildform von [[Obstbäume]]n), oder es sind stachelartig ausgebildete [[Nebenblätter]] wie etwa bei der [[Gewöhnliche Robinie|Gewöhnlichen Robinie]].

Ein Europäischer Laubbaum trägt durchschnittlich 30.000 Blätter. An warmen Sommertagen können so mehrere hundert Liter Wasser zum Kühlen verdunsten.

=== Die Blüten ===

Die [[Blüte]]n der Bäume aus gemäßigten Breiten sind manchmal verhältnismäßig unscheinbar, bei einigen [[Taxon|Taxa]] sind einzelne [[Blüte]]nblattkreise reduziert. Einige Baumarten gemäßigter Breiten haben eingeschlechtliche Blüten. Dabei sitzen die Blüten beider Geschlechter entweder auf demselben Baum (einhäusig) (zum Beispiel [[Eiche]], [[Buche]], [[Hainbuche]], [[Birken|Birke]], [[Erlen (Botanik)|Erle]] und [[Walnussgewächse|Nussbaum]]) oder auf verschiedenen (zweihäusig), so dass man männliche und weibliche Bäume zu unterscheiden hat (unter anderen bei [[Weiden (Botanik)|Weiden]] und [[Pappeln]]). Andere Bäume wie Obstbäume, [[Rosskastanie]] und viele Bäume der wärmeren Klimate haben Zwitterblüten, die sowohl Staub- als auch Fruchtblätter ausbilden.

[[Bild:Eicheln.jpg|thumb|left|Eicheln, die Früchte der Eiche]]

=== Frucht- und Samenbildung ===

Die [[Frucht (Botanik)|Frucht]]- und [[Same (Pflanze)|Samenbildung]] zeigt weniger Eigentümlichkeiten. Bei den meisten Bäumen fällt die Reife in den [[Sommer]] oder [[Herbst]] des selben Jahres; nur bei den Kiefernarten erlangen die Samen und die sie enthaltenden [[Zapfen (Botanik)|Zapfen]] erst im zweiten Herbst nach der Blüte vollständige Ausbildung.
Die Früchte sind meistens [[nuss]]artig mit einem einzigen ausgebildeten Samen, oder sie zerfallen in mehrere einsamige, nussartige Teile, wie bei den [[Ahorne]]n. Saftige [[Steinfrucht|Steinfrüchte]], ebenfalls mit einem oder wenigen Samen, finden sich bei den Obstbäumen, Kapseln mit zahlreichen Samen bei den Weiden und Pappeln.
